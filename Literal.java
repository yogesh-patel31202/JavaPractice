public class Literal{
	public static void main(String []args){
		//*** Literal *** value of Data type 
		//10, 20 , 10.5f, 20.5555555555555d, A, Yogesh
		int num_one=10; //int Data type ki value 10 he
		long num_two=20;
		float float_one=10.5f;
		double float_two=20.55555555555555d;
		char char_a='A';		
		String name="Yogesh";

		
		System.out.println(num_one);//print ke liye
		System.out.println(num_two);
		System.out.println(float_one);
		System.out.println(float_two);
		System.out.println(char_a);
		System.out.println(name);
	}
}