public class DataType{
	public static void main(String []args){
	//*** Data type *** int, long, float, double, char, String
		int num_one=10;
		long num_two=20;
		float float_one=10.5f;
		double float_two=20.555555555555555555555d;
		char char_a='A';		
		String name="Yogesh";

		
		System.out.println(num_one);
		System.out.println(num_two);
		System.out.println(float_one);
		System.out.println(float_two);
		System.out.println(char_a);
		System.out.println(name);
	}
}